package Sets;

public class NavigableSET {


//    NAVIGABLE SET INTERFACE

//    In Java, the NavigableSet is a subtype of the
//    SortedSet interface.

//    NavigableSet is an interface that extends SortedSet and adds extra navigation methods.


//    It allows us to perform various operations
//    like getting the closest matches for a given element, descending order iteration, and others. It provides methods to navigate
//    through the elements in the set.



//    Beyond SortedSet, NavigableSet adds:
//
//    lower(e) → returns the greatest element strictly less than e
//
//    floor(e) → returns the greatest element less than or equal to e
//
//    ceiling(e) → returns the smallest element greater than or equal to e
//
//    higher(e) → returns the smallest element strictly greater than e
//
//    pollFirst() → removes and returns the smallest element
//
//    pollLast() → removes and returns the largest element
//
//    descendingSet() → returns a reverse-order view of the set
//
//    descendingIterator() → iterates elements from largest to smallest

}
